import '../../core/Providers/FB Firestore/fbfirestore_repo.dart';
import '../Model/Student/student.model.dart';

class StudentRepo extends FirestoreRepo<Student> {
  StudentRepo()
      : super(
          'Students',
        );

  @override
  Student? toModel(Map<String, dynamic>? item) => Student.fromMap(item ?? {});

  @override
  Map<String, dynamic>? fromModel(Student? item) => item?.toMap() ?? {};
}
